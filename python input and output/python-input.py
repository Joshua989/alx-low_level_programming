# Printing multiple values separated by '*' using the print() function
print(1, 2, 3, 4, sep='*')

# Output: 1*2*3*4

# Printing multiple values separated by 'a' and ending with '&' using the print() function
print(1, 3, 2, 3, sep='a', end='&')

# Output: 1a3a2a3&

# Printing a formatted string using the format() method
print("I love {} and {}".format("bread", "butter"))

# Output: I love bread and butter


# Taking input from the keyboard
x = int(input("Please type in any number: "))
print(x)

# Output: <user input>

# Always try to specify the data type when taking input

# Example:
x = 5
y = 6
print(int(x) + y)

